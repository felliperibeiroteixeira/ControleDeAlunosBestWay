    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        try {
            if ((txtCurso.getText().isEmpty())) {
                lblCurso.setForeground(Color.red);
                JOptionPane.showMessageDialog(null, "Digite o nome do curso", "Atenção", JOptionPane.ERROR_MESSAGE);
            } else {
                lblCurso.setForeground(Color.black);
                int confirma = JOptionPane.showConfirmDialog(null, "As informações deste curso serão alteradas, deseja prosseguir?", "Atenção", JOptionPane.YES_NO_OPTION);
                if (confirma == JOptionPane.YES_OPTION) {
                    String sql = "update cursos set curso=? where id=?";
                    pst = conexao.prepareStatement(sql);
                    pst.setString(1, txtCurso.getText());
                    pst.setString(2, id.toString());
                    int auterar = pst.executeUpdate();
                    System.out.println(auterar);
                    if (auterar > 0) {
                        PreencherTabela();
                        JOptionPane.showMessageDialog(null, "Curso alterado com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Erro ao alterar", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essse curso?", "Atenção", JOptionPane.YES_NO_OPTION);
            if (confirma == JOptionPane.YES_OPTION) {
                String sql = "delete from cursos where id = ?";
                pst = conexao.prepareStatement(sql);
                pst.setString(1, id.toString());
                int excluido = pst.executeUpdate();
                System.out.println(excluido);
                if (excluido > 0) {
                    PreencherTabela();
                    JOptionPane.showMessageDialog(null, "Curso excluido com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Erro ao excluir", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

