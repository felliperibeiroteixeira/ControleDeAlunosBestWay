    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            if ((txtCurso.getText().isEmpty())) {
                lblCurso.setForeground(Color.red);
                JOptionPane.showMessageDialog(null, "Digite o nome do curso", "Atenção", JOptionPane.ERROR_MESSAGE);
            } else {
                lblCurso.setForeground(Color.black);
                String sql = "insert into cursos(curso) values(?)";
                pst = conexao.prepareStatement(sql);
                pst.setString(1, txtCurso.getText());
                int salvo = pst.executeUpdate();
                System.out.println(salvo);
                if (salvo > 0) {
                    PreencherTabela();
                    JOptionPane.showMessageDialog(null, "Curso salvo com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Erro au salvar", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

