    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essse aluno?", "Aten√ß√£o", JOptionPane.YES_NO_OPTION);
            if (confirma == JOptionPane.YES_OPTION) {
                String sql = "delete from alunos where id = ?";
                pst = conexao.prepareStatement(sql);
                pst.setString(1, id.toString());
                int excluido = pst.executeUpdate();
                System.out.println(excluido);
                if (excluido > 0) {
                    PreencherTabela();
                    JOptionPane.showMessageDialog(null, "Aluno excluido com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Erro ao excluir", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

